/**
 * Build-time syntax highlighting with Shiki.
 * Generates pre-rendered HTML for all code examples.
 * Output is imported as static strings — zero runtime Shiki cost.
 */
import { createHighlighter } from 'shiki'
import { writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))

const codeExamples = {
  hero: {
    lang: 'typescript',
    code: `import { createS402Client } from '@sweefi/sui';

const client = createS402Client({ wallet: myKeypair, network: 'sui:testnet' });
const data = await client.fetch('https://api.example.com/premium-data');
// 402 -> auto-signs SUI payment -> retries -> returns data`,
  },
  agent: {
    lang: 'typescript',
    code: `import { createS402Client } from '@sweefi/sui';
import { Ed25519Keypair } from '@mysten/sui/keypairs/ed25519';

const wallet = Ed25519Keypair.fromSecretKey(myKey);
const client = createS402Client({ wallet, network: 'sui:testnet' });

// Any fetch to a 402-gated endpoint auto-pays
const data = await client.fetch('https://api.example.com/premium');`,
  },
  server: {
    lang: 'typescript',
    code: `import { Hono } from 'hono';
import { s402Gate } from '@sweefi/server';

const app = new Hono();

app.use('/premium', s402Gate({
  price: '1000000',        // 0.001 SUI
  network: 'sui:testnet',
  payTo: '0xYOUR_ADDRESS',
  schemes: ['exact'],
}));

app.get('/premium', (c) => c.json({ data: 'premium content' }));`,
  },
  mcp: {
    lang: 'json',
    code: `{
  "mcpServers": {
    "sweefi": {
      "command": "npx",
      "args": ["@sweefi/mcp"],
      "env": {
        "SUI_NETWORK": "testnet",
        "SUI_PRIVATE_KEY": "<your-base64-key>"
      }
    }
  }
}`,
  },
  cli: {
    lang: 'bash',
    code: `# Install
npm install -g @sweefi/cli

# Create a wallet
sweefi wallet create

# Pay for a 402-gated endpoint
sweefi pay https://sweefi-demo.fly.dev/api/forecast

# Check prepaid balance
sweefi prepaid status <balance-id>`,
  },
}

async function main() {
  const highlighter = await createHighlighter({
    themes: ['tokyo-night'],
    langs: ['typescript', 'json', 'bash'],
  })

  const result = {}

  for (const [key, { lang, code }] of Object.entries(codeExamples)) {
    result[key] = highlighter.codeToHtml(code, {
      lang,
      theme: 'tokyo-night',
    })
  }

  const output = `// AUTO-GENERATED by scripts/generate-highlights.mjs
// Do not edit manually. Run: node scripts/generate-highlights.mjs

${Object.entries(result)
  .map(([key, html]) => `export const ${key}Html = ${JSON.stringify(html)}`)
  .join('\n\n')}
`

  const outPath = resolve(__dirname, '../src/generated/highlights.ts')
  writeFileSync(outPath, output)
  console.log(`Generated highlights -> ${outPath}`)

  highlighter.dispose()
}

main().catch(console.error)
